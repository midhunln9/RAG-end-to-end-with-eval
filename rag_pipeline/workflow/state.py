"""
RAG workflow state definition.

Defines the state schema for the LangGraph workflow.
"""

from typing import Optional

from langchain_core.documents import Document
from langchain_core.messages import BaseMessage
from typing import TypedDict


class AgentState(TypedDict, total=False):
    """
    Workflow state for the RAG pipeline.
    
    Maintains all intermediate and final data throughout the workflow execution.
    
    Attributes:
        query: Original user query.
        rewritten_query: Query rewritten for better retrieval.
        retrieved_documents: Documents retrieved from vector store.
        session_id: Unique identifier for user session.
        conversation_history: List of conversation messages.
        response: Final response generated by LLM.
        summary_before_last_five_messages: Summary of recent conversation context.
    """

    query: str
    rewritten_query: Optional[str]
    retrieved_documents: Optional[list[Document]]
    session_id: str
    conversation_history: Optional[list[BaseMessage]]
    response: Optional[str]
    summary_before_last_five_messages: Optional[str]